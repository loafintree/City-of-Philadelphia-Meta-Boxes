?>

<article id="post-<?php the_ID(); ?>" <?php post_class(); ?>>
	<?php if ( ! twentynineteen_can_show_post_thumbnail() ) : ?>
	<header class="entry-header">
		<?php get_template_part( 'template-parts/header/entry', 'header' ); ?>
	</header>
	<?php endif; ?>

	<div class="entry-content">
		<?php
		the_content(
			sprintf(
				wp_kses(
					/* translators: %s: Name of current post. Only visible to screen readers */
					__( 'Continue reading<span class="screen-reader-text"> "%s"</span>', 'twentynineteen' ),
					array(
						'span' => array(
							'class' => array(),
						),
					)
				),
				get_the_title()
			)
		);
		
		//Display meta_boxes data
		echo '<div><strong>Release Date:</strong> ' . get_post_meta($post->ID, 'philly_release_date', true) . '<br>';
		
			$values = rwmb_meta( 'philly_contact_meta', array() );
			if ( !empty( $values ) ) {
			foreach( $values as $value ) {
				echo '<div class="meta_box"><strong>Contact Name:</strong> ' . $value['contact_name'] . '<br>';
				echo '<strong>Contact Email:</strong> ' . $value['contact_email'] . '<br>';
				echo '<strong>Contact Phone:</strong> ' . $value['contact_phone'] . '</div>';
				}
			}
		
		wp_link_pages(
			array(
				'before' => '<div class="page-links">' . __( 'Pages:', 'twentynineteen' ),
				'after'  => '</div>',
			)
		);
		
		?>
	</div><!-- .entry-content -->

	<footer class="entry-footer">
		<?php twentynineteen_entry_footer(); ?>
	</footer><!-- .entry-footer -->

	<?php if ( ! is_singular( 'attachment' ) ) : ?>
		<?php get_template_part( 'template-parts/post/author', 'bio' ); ?>
	<?php endif; ?>

</article><!-- #post-<?php the_ID(); ?> -->